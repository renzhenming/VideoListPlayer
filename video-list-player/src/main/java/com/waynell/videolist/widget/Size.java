package com.waynell.videolist.widget;

/**
 * Create On 05/01/2017
 * @author Wayne
 */
public class Size {
    private int mWidth;
    private int mHeight;

    public Size(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
